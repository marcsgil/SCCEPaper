    -2x[1],
    -2x[2],
    -8x[3]^3 + 6x[3]*y[1]^2 - 4(μ-x[4])*x[3] - y[1]*y[2],
    2 * ( x[3]^2 - y[1]^2/4 ) - 4x[4]
]

